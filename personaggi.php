        <h3 id="Ponygon" style="color:red">Ponygon e Kafka Sunbeam</h3>
        <img src="images/personaggi/Ponygon.webp" alt="personaggi" class="img-fluid">
        <p>
            Ponygon è un piccolo cavallino momodo di quattro anni che non è in grado di parlare ed è un caro amico
            di Zatch. Il suo vero nome è Schneider, ma nessuno lo capisce, quindi Kiyo lo soprannomina Ponygon.
            I suoi incantesimi riguardano l'armatura e in seguito anche il fuoco e la velocità.
            Grazie all'aiuto del dottor Rebus, Ponygon incontra Kafka Sunbeam, un ingegnere meccanico tedesco,
            nonché l'unico essere umano in grado di capire il suo bizzarro linguaggio e di leggere dal suo libro
            di incantesimi. Ponygon a volte può infuriarsi e può dare un morso a chiunque gli dia fastidio.
            Il suo libro viene bruciato da Megumi su richiesta di Sunbeam, terminando al quarto posto la battaglia
            per il trono. Il suo libro degli incantesimi è color salmone. Nell'anime, il personaggio di Ponygon è
            stato introdotto prima rispetto al manga.<br>
            <b>Potere</b>: Armatura, Fuoco e Velocità<br>
            <b>Colore Libro</b>: Salmone<br>
            <b>Eliminato da:</b> Nessuno(Anime), Megumi Oumi(Manga)<br>

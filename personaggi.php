            È figlio del re mamodo Dauwan Bell, ma perse ogni suo ricordo delle sue origini a causa del suo fratello
            gemello Zeno,
            che, per invidia, gli cancellò la memoria. Zatch è un mamodo generoso, ma anche ingenuo e il suo coraggio
            talvolta gli fa
            compiere azioni imprudenti. Sulla Terra Zatch conosce Kiyo, un geniale studente liceale di 14 anni. I due
            uniscono le forze
            per affrontare la battaglia per determinare il re dei mamodo, e risultano infine la squadra vincente.

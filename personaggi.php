            come Ponygon, non è in grado di pronunciare parole. Alla fine la coppia ha collaborato Ellie e Arth al fine
            di prevenire Faudo dirisurrezione. Kardio viene rimandato nel mondo Mamodo dopo essersi sacrificato per
            sconfiggere il Guardiano del Cuore di Faudo e proteggere Ellie. Karudio ha un'ovvia antipatia per Ponygon.
            I suoi incantesimi gli permettono di guadagnare armature e manipolare il ghiaccio, simile al fuoco di
            Ponygon.<br>
            <b>Potere</b>: Armatura, Ghiaccio<br>
            <b>Colore Libro</b>: Blu Lavaggio<br>
            <b>Eliminato da:</b> Nessuno(Anime), Faudo(Manga)<br>

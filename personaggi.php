        <h3 id="Brago" style="color:red">Brago e Sherry Belmont</h3>
        <img src="images/personaggi/Brago.webp" alt="personaggi" class="img-fluid">
        <p>
            Brago e Sherry Belmont sono i rivali principali dei protagonisti Zatch e Kiyo per il trono dei momodo.
            Brago è uno dei mamodo più potenti, controlla il potere della gravità; ha 14 anni e indossa sempre una
            minacciosa pelliccia nera, del colore del suo libro. Ha avuto una vita molto difficile che lo ha portato
            a sviluppare un atteggiamento ruvido e antisociale e a diventare un temibile guerriero. La sua partner umana
            è Sherry, una ragazza che vive in Francia ed è figlia di una famiglia molto importante; la ragazza odia
            molto
            i mamodo perché un mamodo di nome Zofis ha manipolato il cuore della sua unica amica Koko.
            Durante il corso della battaglia i due bruciano, in modo anche crudele, una grande quantità di libri ma alla
            fine vengono sconfitti da Zatch e Kiyo arrivando secondi. Grazie a Sherry inoltre Brago diventa più gentile
            nel
            corso della battaglia.<br>
            <b>Potere</b>: Gravitá<br>
            <b>Colore Libro</b>: Nero<br>
            <b>Eliminato da:</b> Nessuno(Anime), Zatch Bell(Manga)<br>
        </p><br>

        <h3 id="Tia" style="color:red">Tia e Megumi Oumi</h3>
        <img src="images/personaggi/Tia.webp" alt="personaggi" class="img-fluid">
        <p>
            Tia è una mamodo di sei anni, amica di Zatch, di cui è innamorata. La sua compagna umana è la pop star
            Megumi Oumi, che non combatte ma legge solo gli incantesimi lanciati da Tia. Sono amiche e alleate di Zatch
            e Kiyo. Finiscono il combattimento al quinto posto. Il libro di Tia è di colore arancione. I poteri magici
            di Tia sono di tipo difensivo e rigenerativo, e sono legati alle sue emozioni che se forti li potenzia.<br>
            <b>Potere</b>: Difesa e Rigenerazione<br>
            <b>Colore Libro</b>: Vermiglio<br>
            <b>Eliminato da:</b> Nessuno(Anime), Clear Note(Manga)<br>
        </p><br>

        <h3 id="Kanchome" style="color:red">Kanchome e Parco Folgore</h3>
        <img src="images/personaggi/Kanchome.webp" alt="personaggi" class="img-fluid">
        <p>
            Kanchome e Parco Folgore sono amici e alleati di Zatch e Kiyo.

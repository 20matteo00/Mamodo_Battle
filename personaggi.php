        <h3 id="Momon" style="color:red">Momon e Elle Chivas</h3>
        <img src="images/personaggi/Momon.webp" alt="personaggi" class="img-fluid">
        <p>
            Momon è un mamodo a forma di coniglio; il suo hobby preferito è rubare la biancheria intima alle ragazze;
            la sua partner, Elle, è una suora che odia la violenza. Momon è molto astuto e approfitta spesso del fatto
            che la sua partner odia la violenza per compiere vari scherzi da maniaco. Ogn volta che Momon dice una
            bugia,
            cosa che fa spesso, gli si allunga il muso. Momon ha la capacità di creare trucchi con i suoi incantesimi,
            Momon ha anche la capacità di sentire la presenza di altri mamodo in circolazione.
            Durante la sua ultima apparizione nel manga, ove è stato sconfitto da Roedeux, giurerà a Elle che una volta
            ritornato sul suo pianeta si impegnerà a diventare intelligente come Kiyo, facendo felice Elle.<br>
            <b>Potere</b>: Inganno<br>

            Li-Yen, una ragazza di Hong Kong dall'infanzia travagliata a causa dell'iperprotettività del padre,
            un boss della malavita. I due si innamorano e Wonrei tende a proteggerla da ogni pericolo.
            I loro incantesimi sono basati sulle arti marziali, dato che sia Li-Yen che Wonrei hanno praticato
            kung-fu e il loro libro è di colore viola.<br>

            il suo compagno è l'attore italiano Parco Folgore, un cantante famoso originario di Milano.
            Si piazzano settimi nella battaglia per il re dei momodo.
            Kanchome e Folgore raramente combattono davvero; si concentrano sull'inganno e sulla confusione
            con l'aiuto delle capacità di Kanchomé di cambiare forma. Posseggono un libro degli incantesimi giallo.<br>
            <b>Potere</b>: Trasformazione, Moltiplicazione e Inganno<br>

        <h3 id="Kido" style="color:red">Kido e Dr. Rebus</h3>
        <img src="images/personaggi/Kido.webp" alt="personaggi" class="img-fluid">
        <p>
            Kido è un mamodo di otto anni. È in parte robotico, come dimostrano i suoi vari incantesimi che
            prevedono l'uso di armi, macchine e la modificazione di parti del corpo. Dal punto di vista personale, è
            infantile, presuntuoso e molto credulone. Il suo partner umano, il Dottor Rebus, è
            un ex chirurgo statunitense che ha finito per isolarsi perché non era riuscito a salvare un bambino in
            seguito a un'operazione chirurgica. Rebus agisce spesso come sue mentore, e i due hanno un rapporto come
            quello di un padre e di un figlio. Per via della sua intelligenza, Rebus è la fonte primaria di informazioni
            per i loro alleati. Kido possiede un libro degli incantesimi grigio scuro.<br>
            <b>Potere</b>: Laser e Meccanico<br>
            <b>Colore Libro</b>: Grigio Scuro<br>
            <b>Eliminato da:</b> Belgim E.C.<br>
        </p><br>

        <h3 id="Earth" style="color:red">Earth e Elly</h3>
        <img src="images/personaggi/Earth.webp" alt="personaggi" class="img-fluid">
        <p>
            Ellie è una ragazza che è stata confinata in un letto d'ospedale in attesa della morte per la sua malattia
            terminale. Quindi incontra il suo Mamodo Arth chi le dice che deve continuare a vivere,
            spingendo la creazione della sua personalità maschiaccio. Arth è un saggio Mamodo che desidera diventare
            il re Mamodo per cambiare le leggi del mondo Mamodo per il bene più grande.
            Ellie e Arth furono le prime a essere consapevoli dell'esistenza di Faudo e ha tentato di fermare
            la sua risurrezione. Tuttavia, il Mamodo Rioh mise una maledizione su Ellie costringendo Arth ad aiutare
            a rompere il sigillo a Faudo. Dopo aver rotto il sigillo, Ellie e Arth si alleano con Kiyo per rimandare
            Faudo nel mondo Mamodo. Dopo la vittoria su Faudo, Ellie torna in ospedale dove è stata trovata una cura
            per la sua malattia. Durante la sua permanenza, lei e Arth sono attaccati da Clear Note il servitore Gorm
            e Arth viene inviato nel mondo Mamodo nella loro sconfitta. Il duo era al 10 ° posto.
            Gli incantesimi di Arth erano basati su tecniche di spada.<br>
            <b>Potere</b>: Spada<br>
            <b>Colore Libro</b>: Magenta<br>
            <b>Eliminato da:</b> Riou(Anime), Gorm(Manga)<br>
        </p><br>

        <h3 id="Karudio" style="color:red">Karudio e Sauza</h3>
        <img src="images/personaggi/Karudio.webp" alt="personaggi" class="img-fluid">
        <p>
